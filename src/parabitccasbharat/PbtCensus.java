    void loadTable()
    {
        String qry = "SELECT * FROM pbtempschedule WHERE CEID = '"+oblogin.ceid+"'";
        try         
        {
            db.rs1 = db.stm.executeQuery(qry);
            DefaultTableModel tm = (DefaultTableModel)townvill.getModel();
            tm.setRowCount(0);
            
            while(db.rs1.next())
            {
                int tp = db.rs1.getInt("PopulationAlloted");
                int wd = db.rs1.getInt("WorkDone");
                int pp = tp - wd;
                Object o[] = {db.rs1.getString("City_Vill"), tp, wd, pp};
                tm.addRow(o);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
