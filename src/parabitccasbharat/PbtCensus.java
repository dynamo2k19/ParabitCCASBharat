    @Override
    public void mouseClicked(MouseEvent e) {
        if (e.getClickCount() == 1)
        {
            int r = townvill.getSelectedRow();
            if(townvill.isRowSelected(r) == true)
            {
                btnhouselist.setEnabled(true);
                btnincomplete.setEnabled(true);
            }
            else
            {
                btnhouselist.setEnabled(false);
                btnincomplete.setEnabled(false);
                btnindustry.setEnabled(false);
                btnlandres.setEnabled(false);
            }
            String town = townvill.getValueAt(r, 0).toString();
            oblogin.town = town;
        }
        
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
    }

