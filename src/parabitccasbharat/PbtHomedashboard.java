    private void btnexitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexitActionPerformed
        String note = javax.swing.JOptionPane.showInputDialog("Enter Note");
        int rows = memberstable.getRowCount();
        try
        {
            for (int r = 0; r < rows; r++)
            {
                if (memberstable.getValueAt(r, 5).equals("Y"))
                {
                    db.stm.executeUpdate("UPDATE pbtcensus_household SET Status = '1' WHERE HH_SNo = " + memberstable.getValueAt(r, 1));
                    db.rs1 = db.stm.executeQuery("SELECT WorkDone From pbtempschedule WHERE CEID = '"+PbtLoginData.ceid+"' AND City_Vill = '"+PbtLoginData.town+"'");
                    db.rs1.next();
                    int wd = db.rs1.getInt(1) + 1;
                    db.stm.executeUpdate("UPDATE pbtempschedule SET WorkDone = "+wd+" WHERE CEID = '"+PbtLoginData.ceid+"' AND City_Vill = '"+PbtLoginData.town+"'");
                }
                if (memberstable.getValueAt(r, 5).equals("N"))
                {
                    db.stm.executeUpdate("UPDATE pbtcensus_household SET Status = '7' WHERE HH_SNo = " + memberstable.getValueAt(r, 1));
                }
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        this.dispose();
        enumdash.setVisible(true);
    }//GEN-LAST:event_btnexitActionPerformed

