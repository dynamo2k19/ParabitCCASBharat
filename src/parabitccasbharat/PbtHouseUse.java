    private int checkPending()
    {
        String latilongi = tflatitude.getText() + " , " + tflongitude.getText();
        String qry = "SELECT HL_SNo FROM pbtcensus_houselisting WHERE latilongi = '" + latilongi + "' AND Status = '8'";
        try
        {
            db.rs2 = db.stm.executeQuery(qry);
            if (db.rs2.next())
            {
                return db.rs2.getInt(1);
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        
        return 0;
    }
