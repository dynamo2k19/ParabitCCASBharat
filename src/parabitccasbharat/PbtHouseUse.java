    private void btnproceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnproceedActionPerformed
        int hlsno = checkPending();
        if (hlsno == 0)
        {
            String latilongi = tflatitude.getText() + " , " + tflongitude.getText();
            String qry = "INSERT INTO pbtcensus_houselisting (UseOfHouse, OwnerPhnNo, LatiLongi) VALUES('"+btnresidence.getText()+"','"+tfmob.getText()+"','"+latilongi+"')";
            String qry2 = "SELECT HL_SNo FROM pbtcensus_houselisting ORDER BY HL_SNo DESC LIMIT 1";

            try
            {
                db.stm.executeUpdate(qry);
                db.rs1 = db.stm.executeQuery(qry2);
                db.rs1.next();
                hlsno = db.rs1.getInt(1);
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
        PbtHouseType ob = new PbtHouseType(enumdash, oblogin, hlsno);
        ob.n = n;
        new PbtUseOfHouse(n);
        ob.visible();
        this.dispose();
        ob.setVisible(true);
            
    }//GEN-LAST:event_btnproceedActionPerformed

