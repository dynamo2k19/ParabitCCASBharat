    private void btncalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalActionPerformed
        if (tfareasq.getText().isEmpty() == false)
        {
            float areads = Integer.parseInt(tfareasq.getText()) / 435.6f;
            tfareads.setText(areads+"");
        }
        else
        {
            javax.swing.JOptionPane.showMessageDialog(null, "Provide Area in square feet!");
        }
    }//GEN-LAST:event_btncalActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        int landtype = lt.getSelectedIndex()+1;
        String qry = "INSERT INTO landreg VALUES('"+cod.getSelectedItem()+"','"+tfclmtano.getText()+"','"+tfexano.getText()+"','"+tfiano.getText()+"','"+tfsro.getText()+"',"
                + "'"+tftehsil.getText()+"','"+tfvill.getText()+"','"+tfkhata.getText()+"','"+tfplot.getText()+"','"+pt.getSelectedItem()+"',"+landtype+","+tfareasq.getText()+","+tfareads.getText()+","
                + "'"+tflati.getText()+"','"+tflongi.getText()+"'";
        try
        {
            db.stm.executeUpdate(qry);
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        this.dispose();
        obcen.setVisible(true);
    }//GEN-LAST:event_btnsaveActionPerformed

