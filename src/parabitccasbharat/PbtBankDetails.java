    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if (rbnetyes.isSelected())
        {
            net = "Y";
        }
        else
        {
            net = "N";
        }
        if (rbmobyes.isSelected())
        {
            mob = "Y";
        }
        else
        {
            mob = "N";
        }
        String qry = "UPDATE pbtcensus_household SET IFSC = '"+tfifsc.getText()+"',BankAccNo = '"+tfbank.getText()+"',NetBank = '"+net+"',"
                + "MobBank = '"+mob+"' WHERE HH_SNo = "+hhsno;
        System.out.println(qry);
        try
        {
            db.stm.executeUpdate(qry);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        javax.swing.JOptionPane.showMessageDialog(null, "Data Saved Successfully!!!");
        this.dispose();
    }//GEN-LAST:event_btnsaveActionPerformed

