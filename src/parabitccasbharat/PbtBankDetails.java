    public void incomp()
    {
        try
        {
            tfifsc.setText(obperson.db.rs2.getString("IFSC"));
            tfbank.setText(obperson.db.rs2.getString("BankAccNo"));
            if (obperson.db.rs2.getString("NetBank").equals("Y"))
            {
                rbnetyes.setSelected(true);
            }
            else if (obperson.db.rs2.getString("NetBank").equals("N"))
            {
                rbnetno.setSelected(true);
            }
            
            if (obperson.db.rs2.getString("MobBank").equals("Y"))
            {
                rbmobyes.setSelected(true);
            }
            else if (obperson.db.rs2.getString("MobBank").equals("N"))
            {
                rbmobno.setSelected(true);
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }

