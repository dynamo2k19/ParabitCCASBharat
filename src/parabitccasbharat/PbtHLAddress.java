    public PbtHLAddress(PbtHouselisting ob, int hlno, PbtLoginData oblog) {
        super(ob, true);
        setTitle("Address");
        initComponents();
        db = new ParabitDBC();
        hlsno = hlno;
        String qry = "SELECT * FROM pbtempschedule WHERE CEID = '" + oblog.ceid + "' AND City_Vill = '" + oblog.town + "'";
        try
        {
            db.rs1 = db.stm.executeQuery(qry);
            System.out.println(qry);
            while (db.rs1.next())
            {
                tfstut.setText(db.rs1.getString("State"));
                tfwardno.setText(db.rs1.getString("WardNo"));
                tfdist.setText(db.rs1.getString("Dist"));
                tftehsil.setText(db.rs1.getString("Tehsil"));
                tftown.setText(db.rs1.getString("City_Vill"));
            }
        } 
        catch (SQLException ex) 
        {
            ex.printStackTrace();
        }
        
    }

