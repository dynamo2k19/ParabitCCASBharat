    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if (sysyes.isSelected())
        {
            system = "Y";
        }
        else
        {
            system = "N";
        }
        if (balarmyes.isSelected())
        {
            balarm = "Y";
        }
        else
        {
            balarm = "N";
        }
        if (falarmyes.isSelected())
        {
            falarm = "Y";
        }
        else
        {
            falarm = "N";
        }
        
        String qry = "UPDATE pbtcensus_houselisting SET SmartSecuritySystem = '"+system+"',CCTVIPAdress = '"+tfcctv.getText()+"',FireExtinguisher = "+fireexting.getValue()+""
                + ",FireAlarm = '"+falarm+"',PrivateSecurityGuard = "+pvtsecurity.getValue()+",PrivateLicenseArms = "+pvtarms.getValue()+",BurglarAlarm = '"+balarm+"' WHERE HL_SNo = "+hlsno;
        
        System.out.println(qry);
        try
        {
            db.stm.executeUpdate(qry);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        javax.swing.JOptionPane.showMessageDialog(null, "Data Saved Successfully!!!");
        this.dispose();
    }//GEN-LAST:event_btnsaveActionPerformed

