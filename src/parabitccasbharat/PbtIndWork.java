    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        String pe = "N";
        if (yes.isSelected() == true)
        {
            pe = "Y";
        }
        String qry = "UPDATE pbtindustry SET TurnOverOfComp = "+tfturnover.getText()+", FnlProdOfComp = '"+tfproduct.getText()+"', NoOfMaleEmp = "+tfmale.getText()+", "
                + "NoOfFemEmp = "+tffemale.getText()+", NoOfOtherEmp = "+tfotheremp.getText()+", MaxSalOfComp = "+tfmaxsal.getText()+", MinSalOfComp = "+tfminsal.getText()+", "
                + "NoOfShift = "+tfshifts.getText()+", ExportProd = '"+pe+"' WHERE ICSNo = "+obdash.icsno;

        try
        {
            db.stm.executeUpdate(qry);
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        this.dispose();
    }//GEN-LAST:event_btnsaveActionPerformed

