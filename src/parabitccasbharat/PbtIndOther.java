    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int induswater = watersrc1.getSelectedIndex() + 1;
        int drkwater = watersrc2.getSelectedIndex() + 1;
        int lsrc = lightsrc.getSelectedIndex() + 1;
        String cant = "N", med = "N";
        if (cantyes.isSelected() == true)
        {
            cant = "Y";
        }
        if (medyes.isSelected() == true)
        {
            med = "Y";
        }
        String qry = "UPDATE pbtindustry SET MainSrcLight = '"+lsrc+"', MainSrc_WatForIndus = '"+induswater+"', MainSrc_DrnkWat_inIndus = '"+drkwater+"', NoOfWash_inIndus = "+tfwashroom.getText()+", AvbOfCant = '"+cant+"', AvbOfMedFac_InsdComp = '"+med+"', NearestPS = '"+tfpolice.getText()+"', NearestHsp = '"+tfhospital.getText()+"', NearestFireBrgd = '"+tffire.getText()+"', NearestRail = '"+tfrailway.getText()+"',"
                + "NearestBusStp = '"+tfbus.getText()+"', NearestArprt = '"+tfairport.getText()+"', NearestTrnsprtAr = '"+tftransport.getText()+"' WHERE ICSNo = "+obdash.icsno;

        try
        {
            db.stm.executeUpdate(qry);
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

