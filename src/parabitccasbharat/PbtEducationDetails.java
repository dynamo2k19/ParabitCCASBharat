    public void incomp()
    {
        try
        {
            if (obperson.db.rs2.getString("LitStatus").equals("Y"))
            {
                rbliterate.setSelected(true);
            }
            else if (obperson.db.rs2.getString("LitStatus").equals("N"))
            {
                rbiliterate.setSelected(true);
            }
            tfmothertongue.setText(obperson.db.rs2.getString("MTongue"));
            tfrwlang.setText(obperson.db.rs2.getString("RWLang"));
            tfhomelang.setText(obperson.db.rs2.getString("HomeLang"));
            tfcurrinstitute.setText(obperson.db.rs2.getString("CInstNmCity"));
            tftechdegree.setText(obperson.db.rs2.getString("TechDeg"));
            tfcurrinstitutetype.setText(obperson.db.rs2.getString("CInstType"));
            tfnontechdegree.setText(obperson.db.rs2.getString("NonTechDeg"));
            if (obperson.db.rs2.getString("NCCNSS").equals("Y"))
            {
                rbyes.setSelected(true);
            }
            else if (obperson.db.rs2.getString("NCCNSS").equals("N"))
            {
                rbno.setSelected(true);
            }
            cedustatus.setSelectedIndex(Integer.parseInt(obperson.db.rs2.getString("CEduStatus")));
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }
