    private void tfbtnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfbtnsaveActionPerformed
        if (rbyes.isSelected())
        {
            nccnss = "Y";
        }
        if (rbno.isSelected())
        {
            nccnss = "N";
        }
        if (rbliterate.isSelected())
        {
            lit = "Y";
        }
        if (rbiliterate.isSelected())
        {
            lit = "N";
        }
           
        currentedu = cedustatus.getSelectedIndex()+"";
                
        String qry = "UPDATE pbtcensus_household SET LitStatus = '"+lit+"',CEduStatus = '"+currentedu+"',CInstNmCity = '"+tfcurrinstitute.getText()+"',"
                + "TechDeg = '"+tftechdegree.getText()+"',NonTechDeg = '"+tfnontechdegree.getText()+"',NCCNSS = '"+nccnss+"',"
                + "MTongue = '"+tfmothertongue.getText()+"',RWLang = '"+tfrwlang.getText()+"',HomeLang = '"+tfhomelang.getText()+"',"
                + "CInstType = '"+tfcurrinstitutetype.getText()+"' WHERE HH_SNo = "+hhsno;
        System.out.println(qry);
        try
        {
            db.stm.executeUpdate(qry);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        javax.swing.JOptionPane.showMessageDialog(null, "Data Saved Successfully!!!");
        this.dispose();
    }//GEN-LAST:event_tfbtnsaveActionPerformed

